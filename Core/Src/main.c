void delay(uint16_t time) //HAM DELAY uS
{
	__HAL_TIM_SET_COUNTER(&htim1, 0);
	while(__HAL_TIM_GET_COUNTER(&htim1) < time);
}

uint32_t IC_Val1 = 0; //Bien cho HCSR04
uint32_t IC_Val2 = 0;
uint32_t Difference = 0;
uint8_t Is_First_Captured = 0;  // is the first value captured ?
uint32_t Distance  = 100;

#define TRIG_PIN GPIO_PIN_11
#define TRIG_PORT GPIOA

// Let's write the callback function

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) //CODE SU DUNG HCSR04
{
	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_3)  // if the interrupt source is channel1
	{
		if (Is_First_Captured==0) // if the first value is not captured
		{
			IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_3); // read the first value
			Is_First_Captured = 1;  // set the first captured as true
			// Now change the polarity to falling edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_3, TIM_INPUTCHANNELPOLARITY_FALLING);
		}

		else if (Is_First_Captured==1)   // if the first is already captured
		{
			IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_3);  // read second value
			__HAL_TIM_SET_COUNTER(htim, 0);  // reset the counter

			if (IC_Val2 > IC_Val1)
			{
				Difference = IC_Val2-IC_Val1;
			}

			else if (IC_Val1 > IC_Val2)
			{
				Difference = (0xffff - IC_Val1) + IC_Val2;
			}

			Distance = Difference * .034/2;
			Is_First_Captured = 0; // set it back to false

			// set polarity to rising edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_3, TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(&htim1, TIM_IT_CC3);
		}
	}
}

void HCSR04_Read (void)
{
	HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_SET);  // pull the TRIG pin HIGH
	delay(10);  // wait for 10 us
	HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_RESET);  // pull the TRIG pin low

	__HAL_TIM_ENABLE_IT(&htim1, TIM_IT_CC3);
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) //CODE NHAN TIN HIEU BLUETOOTH
{
		if(Rx_data[0] == 'w') state_led = 0;	//disable
		if(Rx_data[0] == 'W') state_led = 1;	//enable
		//if(Rx_data[0] == 'A') state_led = 2;	//avoiding car
		if(Rx_data[0] == 'B') state = 2;	//lui
		if(Rx_data[0] == 'L') state = 4;	//trai
		if(Rx_data[0] == 'R') state = 6;	//phai
		if(Rx_data[0] == 'F') state = 8;	//tien
		if(Rx_data[0] == 'S') state = 0;	//dung
		if(Rx_data[0] == 'G') state = 7;	// thang + quep trai
		if(Rx_data[0] == 'I') state = 9;	// thang + queo phai
		if(Rx_data[0] == 'H') state = 1;	//lui + queo trai
		if(Rx_data[0] == 'J') state = 3;  //lui + queo phai
		HAL_UART_Receive_IT(&huart2, (uint8_t*)Rx_data, 1);
}

void dieukhien() //CODE DIEU KHIEN
{
		HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, 0);
		HCSR04_Read();
		HAL_Delay(15);
		if(Distance >= 30)
		{
			switch(state)
			{
				case 8:
					htim3.Instance->CCR1 = 90;
					htim3.Instance->CCR2 = 0;
					htim4.Instance->CCR2 = 90;
					htim4.Instance->CCR1 = 0;
				break;
				
				case 6:
					htim3.Instance->CCR2 = 0; 
					htim3.Instance->CCR1 = 45;
					htim4.Instance->CCR1 = 45; 
					htim4.Instance->CCR2 = 0;
				break;
				
				case 4:
					htim3.Instance->CCR2 = 45; 
					htim3.Instance->CCR1 = 0;
					htim4.Instance->CCR1 = 0; 
					htim4.Instance->CCR2 = 45;
				break;
				
				case 2:
					htim3.Instance->CCR2 = 90;
					htim3.Instance->CCR1 = 0;
					htim4.Instance->CCR1 = 90;
					htim4.Instance->CCR2 = 0;
				break;
				
				case 7:
					htim3.Instance->CCR2 = 0; 
					htim3.Instance->CCR1 = 40;
					htim4.Instance->CCR1 = 0; 
					htim4.Instance->CCR2 = 90;
				break;
				
				case 9:
					htim3.Instance->CCR2 = 0;
					htim3.Instance->CCR1 = 90;
					htim4.Instance->CCR1 = 0; 
					htim4.Instance->CCR2 = 40;
				break;
				
				case 1:
					htim3.Instance->CCR2 = 90; 
					htim3.Instance->CCR1 = 0;
					htim4.Instance->CCR1 = 40; 
					htim4.Instance->CCR2 = 0;
				break;
				
				case 3:
					htim3.Instance->CCR2 = 40; 
					htim3.Instance->CCR1 = 0;
					htim4.Instance->CCR1 = 90; 
					htim4.Instance->CCR2 = 0;
				break;
				
				case 0:
					htim3.Instance->CCR2 = 0;
					htim3.Instance->CCR1 = 0;
					htim4.Instance->CCR1 = 0;
					htim4.Instance->CCR2 = 0;
				break;
			}
		}
		else
		{
			if(state == 2)
			{
					htim3.Instance->CCR2 = 90;
					htim3.Instance->CCR1 = 0;
					htim4.Instance->CCR1 = 90;
					htim4.Instance->CCR2 = 0;
			}
			else
			{
					htim3.Instance->CCR2 = 0;
					htim3.Instance->CCR1 = 0;
					htim4.Instance->CCR1 = 0;
					htim4.Instance->CCR2 = 0;
			}
		}
}

